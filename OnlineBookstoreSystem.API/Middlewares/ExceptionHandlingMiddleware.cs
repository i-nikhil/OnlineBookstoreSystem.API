using Newtonsoft.Json;
using OnlineBookstoreSystem.API.Exceptions;
using System.Net;

namespace OnlineBookstoreSystem.API.Middlewares;

public class ExceptionHandlingMiddleware
{
    private readonly RequestDelegate _next;

    public ExceptionHandlingMiddleware(RequestDelegate next)
    {
        _next = next;
    }

    public async Task InvokeAsync(HttpContext context)
    {
        try
        {
            await _next(context);
        }
        catch (Exception ex)
        {
            await HandleExceptionAsync(context, ex);
        }
    }

    private static Task HandleExceptionAsync(HttpContext context, Exception exception)
    {
        var code = exception switch
        {
            BookNotFoundException or UserNotFoundException => HttpStatusCode.NotFound,
            InvalidPriceException or InvalidStockException or EmptyCartException => HttpStatusCode.BadRequest,
            _ => HttpStatusCode.InternalServerError,
        };

        var result = JsonConvert.SerializeObject(new { error = exception.Message });
        context.Response.ContentType = "application/json";
        context.Response.StatusCode = (int)code;
        return context.Response.WriteAsync(result);
    }
}
